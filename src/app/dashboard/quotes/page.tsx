import { getCurrentCompany } from "@/lib/session";
import prisma from "@/lib/prisma";

export default async function QuotesListPage() {
  const company = await getCurrentCompany();
  
  const quotes = await prisma.quoteRequest.findMany({
    where: { companyId: company.id },
    orderBy: { createdAt: "desc" },
  });

  return (
    <div className="p-8">
      <div className="mb-8">
        <h1 className="text-2xl font-bold text-slate-900">Quote Requests</h1>
        <p className="text-slate-500">Track and manage all delivery estimates generated by your widget.</p>
      </div>

      <div className="bg-white rounded-xl border border-slate-200 shadow-sm overflow-hidden">
        <table className="w-full border-collapse text-left">
          <thead>
            <tr className="bg-slate-50 border-b border-slate-200">
              <th className="px-6 py-4 text-xs font-bold text-slate-500 uppercase tracking-wider">Date</th>
              <th className="px-6 py-4 text-xs font-bold text-slate-500 uppercase tracking-wider">Customer</th>
              <th className="px-6 py-4 text-xs font-bold text-slate-500 uppercase tracking-wider">Route</th>
              <th className="px-6 py-4 text-xs font-bold text-slate-500 uppercase tracking-wider">Price Estimate</th>
              <th className="px-6 py-4 text-xs font-bold text-slate-500 uppercase tracking-wider">Contact</th>
            </tr>
          </thead>
          <tbody className="divide-y divide-slate-100">
            {quotes.length === 0 ? (
              <tr>
                <td colSpan={5} className="px-6 py-12 text-center text-slate-500">
                  No quotes found. Make sure your widget is embedded and active.
                </td>
              </tr>
            ) : (
              quotes.map((quote) => (
                <tr key={quote.id} className="hover:bg-slate-50 transition-colors">
                  <td className="px-6 py-4 text-sm text-slate-900">
                    {new Date(quote.createdAt).toLocaleDateString()}
                  </td>
                  <td className="px-6 py-4">
                    <p className="text-sm font-bold text-slate-900">{quote.customerName}</p>
                    <p className="text-xs text-slate-500">{quote.serviceType}</p>
                  </td>
                  <td className="px-6 py-4">
                    <p className="text-sm text-slate-900">{quote.pickupZip} &rarr; {quote.dropoffZip}</p>
                    <p className="text-xs text-slate-500">{quote.distanceMiles.toFixed(1)} miles</p>
                  </td>
                  <td className="px-6 py-4 text-sm font-extrabold text-blue-600">
                    ${quote.estimatedPrice.toFixed(2)}
                  </td>
                  <td className="px-6 py-4">
                    <p className="text-xs font-medium text-slate-900">{quote.customerEmail}</p>
                    <p className="text-xs text-slate-500">{quote.customerPhone || "No phone"}</p>
                  </td>
                </tr>
              ))
            )}
          </tbody>
        </table>
      </div>
    </div>
  );
}
